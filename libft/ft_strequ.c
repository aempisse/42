#include "libft.h"

int		ft_strequ(const char *s1, const char *s2)
{
	if ((!s1 && s2) || (s1 && !s2))
		return (0);
	if (!s1 && !s2)
		return (1);
	if (ft_strcmp(s1, s2) == 0)
		return (1);
	return (0);
}
